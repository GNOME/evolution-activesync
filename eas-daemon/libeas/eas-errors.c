static gpointer
setup_error_map (gpointer data)
{
	gint i;

	eas_error_hash = g_hash_table_new	(g_str_hash, g_str_equal);
	for (i = 0; i < G_N_ELEMENTS(eas_conn_errors); i++)
		g_hash_table_insert	(eas_error_hash, (gpointer) eas_conn_errors[i].error_id, 
					 GINT_TO_POINTER (eas_conn_errors[i].error_code));
	return NULL;
}

gint
eas_get_error_code (const gchar *str)
{
	gint error_code = EAS_CONNECTION_ERROR_UNKNOWN;
	gpointer data;

	g_once (&setup_error_once, setup_error_map, NULL);

	data = g_hash_table_lookup (eas_error_hash, (gconstpointer) str);
	if (data)
		error_code = GPOINTER_TO_INT (data);

	return error_code;
}
